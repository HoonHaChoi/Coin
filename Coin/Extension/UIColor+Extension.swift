import UIKit

extension UIColor {
    
    
    // MARK: BackgroundColor
    static var fallColor: UIColor {
        return UIColor(named: "FALLColor")!
    }
    static var riseColor: UIColor {
        return UIColor(named: "RISEColor")!
    }
    
    // MARK: FontColor
    static var basicColor: UIColor {
        return UIColor(named: "191919")!
    }
    static var middleGrayColor: UIColor {
        return UIColor(named: "767676")!
    }
    static var weakGrayColor: UIColor {
        return UIColor(named: "999999")!
    }
}
